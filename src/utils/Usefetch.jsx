import React from "react";
import { useWeb3ExecuteFunction } from "react-moralis";
import { factoryABI } from "../abi";

export default function Usefetch(otheroption) {
  const { data, error, fetch, isFetching, isLoading } =
    useWeb3ExecuteFunction({
        abi: factoryABI,
        contractAddress: process.env.REACT_APP_FACTORY_CONTRACT_ADD,
        ...otheroption
      });

  return {data, error, fetch, isFetching, isLoading};
}
